export const DEFAULT_LANGUAGE: LanguageCode = 'zh-CN';

export function getLanguageName(code: string): string {
  const lang = SUPPORTED_LANGUAGES.find(l => l.code === code);
  return lang ? `${lang.name} (${lang.nativeName})` : code;
}
